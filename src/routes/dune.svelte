	<p>
		<b> Chapter One <br /></b>
		<i
			><br />A beginning is the time for taking the most delicate care that the balances are
			correct. This every sister of the Bene Gesserit knows. To begin your study of the life of
			Muad'Dib, then, take care that you first place him in his time: born in the 57th year of the
			Padishah Emperor, Shaddam IV. And take the most special care that you locate Muad'Dib in his
			place: the planet Arrakis. Do not be deceived by the fact that he was born on Caladan and
			lived his first fifteen years there. Arrakis, the planet known as Dune, is forever his place.</i
		><br /> —<b>from "Manual of Muad'Dib"<br /> by the Princess Irulan</b>
	</p>
	<p>
		&nbsp; &nbsp; In the week before their departure to Arrakis, when all the final scurrying about
		had reached a nearly unbearable frenzy, an old crone came to visit the mother of the boy, Paul.
	</p>
	<p>
		&nbsp;&nbsp;&nbsp; It was a warm night at Castle Caladan, and the ancient pile of stone that had
		served the Atreides family as home for twenty-six generations bore that cooled-sweat feeling it
		acquired before a change in the weather.
	</p>
	<p>
		&nbsp;&nbsp;&nbsp; The old woman was let in by the side door down the vaulted passage by Paul's
		room and she was allowed a moment to peer in at him where he lay in his bed.
	</p>
	<p>
		&nbsp;&nbsp;&nbsp; By the half-light of a suspensor lamp, dimmed and hanging near the floor, the
		awakened boy could see a bulky female shape at his door, standing one step ahead of his mother.
		The old woman was a witch shadow—hair like matted spiderwebs, hooded 'round darkness of
		features, eyes like glittering jewels.
	</p>
	<p>
		&nbsp;&nbsp;&nbsp; "Is he not small forhis age, Jessica?" the old woman asked. Her voice wheezed
		and twanged like an untuned baliset.
	</p>
	<p>
		&nbsp;&nbsp;&nbsp; Paul's mother answered in her soft contralto: "The Atreides are known to
		start late getting their growth, Your Reverence."
	</p>
	<p>
		&nbsp;&nbsp;&nbsp; "So I've heard, so I've heard," wheezed the old woman. "Yet he's already
		fifteen."
	</p>
	<p>&nbsp;&nbsp;&nbsp; "Yes, Your Reverence."</p>
	<p>
		&nbsp;&nbsp;&nbsp; "He's awake and listening to us," said the old woman. "Sly little rascal."
		She chuckled. "But royalty has need of slyness. And if he's really the Kwisatz Haderach ...
		well...."
	</p>
	<p>
		&nbsp;&nbsp;&nbsp; Within the shadows of his bed, Paul held his eyes open to mere slits. Two
		bird-bright ovals—the eyes of the old woman—seemed to expand and glow as they stared into his.
	</p>
	<p>
		&nbsp;&nbsp;&nbsp; "Sleep well, you sly little rascal," said the old woman. "Tomorrow you'll
		need all your faculties to meet my gom jabbar."
	</p>
	<p>
		&nbsp;&nbsp;&nbsp; And she was gone, pushing his mother out, closing the door with a solid
		thump.
	</p>
	<p>&nbsp;&nbsp;&nbsp; Paul lay awake wondering: <i>What's a gom jabbar?</i></p>
	<p>
		&nbsp;&nbsp;&nbsp; In all the upset during this time of change, the old woman was the strangest
		thing he had seen.
	</p>
	<p>&nbsp;&nbsp;&nbsp; <i>Your Reverence.</i></p>
	<p>
		&nbsp;&nbsp;&nbsp; And the way she called his mother Jessica like a common serving wench instead
		of what she was—a Bene Gesserit Lady, a duke's concubine and mother of the ducal heir.
	</p>
	<p>
		&nbsp;&nbsp;&nbsp; <i>Is a gom jabbar something of Arrakis I must know before we go there?</i> he
		wondered.
	</p>
	<p>&nbsp;&nbsp;&nbsp; He mouthed her strange words: <i>Gom jabbar ... Kwisatz Haderach</i>.</p>
	<p>
		&nbsp;&nbsp;&nbsp; There had been so many things to learn. Arrakis would be a place so different
		from Caladan that Paul's mind whirled with the new knowledge. <i>Arrakis—Dune—Desert Planet</i>.
	</p>
	<p>
		&nbsp;&nbsp;&nbsp; Thufir Hawat, his father's Master of Assassins, had explained it: their
		mortal enemies, the Harkonnens, had been on Arrakis eighty years, holding the planet in
		quasi-fief under a CHOAM Company contract to mine the geriatric spice, melange. Now the
		Harkonnens were leaving to be replaced by the House of Atreides in fief-complete—an apparent
		victory for the Duke Leto. Yet, Hawat had said, this appearance contained the deadliest peril,
		for the Duke Leto was popular among the Great Houses of the Landsraad.
	</p>
	<p>&nbsp;&nbsp;&nbsp; "A popular man arouses the jealousy of the powerful," Hawat had said.</p>
	<p>&nbsp;&nbsp;&nbsp; <i>Arrakis—Dune—Desert Planet.</i></p>
	<p>
		&nbsp;&nbsp;&nbsp; Paul fell asleep to dream of an Arrakeen cavern, silent people all around him
		moving in the dim light of glowglobes. It was solemn there and like a cathedral as he listened
		to a faint sound—the drip-drip-drip of water. Even while he remained in the dream, Paul knew he
		would remember it upon awakening. He always remembered the dreams that were predictions.
	</p>
	<p>&nbsp;&nbsp;&nbsp; The dream faded.</p>
	<p>
		&nbsp;&nbsp;&nbsp; Paul awoke to feel himself in the warmth of his bed—thinking ... thinking.
		This world of Castle Caladan, without play or companions his own age, perhaps did not deserve
		sadness in farewell. Dr. Yueh, his teacher, had hinted that the faufreluches class system was
		not rigidly guarded on Arrakis. The planet sheltered people who lived at the desert edge without
		caid or bashar to command them: will-o'-the-sand people called Fremen, marked down on no census
		of the Imperial Regate.
	</p>
	<p>&nbsp;&nbsp;&nbsp; <i>Arrakis—Dune—Desert Planet.</i></p>
	<p>
		&nbsp;&nbsp;&nbsp; Paul sensed his own tensions, decided to practice one of the mind-body
		lessons his mother had taught him. Three quick breaths triggered the responses: he fell into the
		floating awareness ... focusing the consciousness ... aortal dilation ... avoiding the unfocused
		mechanism of consciousness ... to be conscious by choice ... blood enriched and swift-flooding
		the overload regions ... <i>one does not obtain food-safety-freedom by instinct alone</i> ... animal
		consciousness does not extend beyond the given moment nor into the idea that its victims may become
		extinct ... the animal destroys and does not produce ... animal pleasures remain close to sensation
		levels and avoid the perceptual ... the human requires a background grid through which to see his
		universe ... focused consciousness by choice, this forms your grid ... bodily integrity follows nerve-blood
		flow according to the deepest awareness of cell needs ... all things/cells/beings are impermanent
		... strive for flow-permanence within....
	</p>
	<p>
		&nbsp;&nbsp;&nbsp; Over and over and over within Paul's floating awareness the lesson rolled.
	</p>
	<p>
		&nbsp;&nbsp;&nbsp; When dawn touched Paul's window sill with yellow light, he sensed it through
		closed eyelids, opened them, hearing then the renewed bustle and hurry in the castle, seeing the
		familiar patterned beams of his bedroom ceiling.
	</p>
	<p>
		&nbsp;&nbsp;&nbsp; The hall door opened and his mother peered in, hair like shaded bronze held
		with black ribbon at the crown, her oval face emotionless and green eyes staring solemnly.
	</p>
	<p>&nbsp;&nbsp;&nbsp; "You're awake," she said. "Did you sleep well?"</p>
	<p>&nbsp;&nbsp;&nbsp; "Yes."</p>
	<p>
		&nbsp;&nbsp;&nbsp; He studied the tallness of her, saw the hint of tension in her shoulders as
		she chose clothing for him from the closet racks. Another might have missed the tension, but she
		had trained him in the Bene Gesserit Way—in the minutiae of observation. She turned, holding a
		semiformal jacket for him. It carried the red Atreides hawk crest above the breast pocket.
	</p>
	<p>&nbsp;&nbsp;&nbsp; "Hurry and dress," she said. "Reverend Mother is waiting."</p>
	<p>&nbsp;&nbsp;&nbsp; "I dreamed of her once," Paul said. "Who is she?"</p>
	<p>
		&nbsp;&nbsp;&nbsp; "She was my teacher at the Bene Gesserit school. Now, she's the Emperor's
		Truthsayer. And Paul...." She hesitated. "You must tell her about your dreams."
	</p>
	<p>&nbsp;&nbsp;&nbsp; "I will. Is she the reason we got Arrakis?"</p>
	<p>
		&nbsp;&nbsp;&nbsp; "We did not <i>get</i> Arrakis." Jessica flicked dust from a pair of trousers,
		hung them with the jacket on the dressing stand beside his bed. "Don't keep Reverend Mother waiting."
	</p>
	<p>&nbsp;&nbsp;&nbsp; Paul sat up, hugged his knees. "What's a gom jabbar?"</p>
	<p>
		&nbsp;&nbsp;&nbsp; Again, the training she had given him exposed her almost invisible
		hesitation, a nervous betrayal he felt as fear.
	</p>
	<p>
		&nbsp;&nbsp;&nbsp; Jessica crossed to the window, flung wide the draperies, stared across the
		river orchards toward Mount Syubi. "You'll learn about ... the gom jabbar soon enough," she
		said.
	</p>
	<p>&nbsp;&nbsp;&nbsp; He heard the fear in her voice and wondered at it.</p>
	<p>
		&nbsp;&nbsp;&nbsp; Jessica spoke without turning. "Reverend Mother is waiting in my morning
		room. Please hurry."
	</p>
	<p>&nbsp;</p>
	<p>
		The Reverend Mother Gaius Helen Mohiam sat in a tapestried chair watching mother and son
		approach. Windows on each side of her overlooked the curving southern bend of the river and the
		green farmlands of the Atreides family holding, but the Reverend Mother ignored the view. She
		was feeling her age this morning, more than a little petulant. She blamed it on space travel and
		association with that abominable Spacing Guild and its secretive ways. But here was a mission
		that required personal attention from a Bene Gesserit-with-the-Sight. Even the Padishah
		Emperor's Truthsayer couldn't evade that responsibility when the duty call came.
	</p>
	<p>
		&nbsp;&nbsp;&nbsp; <i
			>Damn that Jessica! the Reverend Mother thought. If only she'd borne us a girl as she was
			ordered to do!</i
		>
	</p>
	<p>
		&nbsp;&nbsp;&nbsp; Jessica stopped three paces from the chair, dropped a small curtsy, a gentle
		flick of left hand along the line of her skirt. Paul gave the short bow his dancing master had
		taught—the one used "when in doubt of another's station."
	</p>
	<p>
		&nbsp;&nbsp;&nbsp; The nuances of Paul's greeting were not lost on the Reverend Mother. She
		said: "He's a cautious one, Jessica."
	</p>
	<p>
		&nbsp;&nbsp;&nbsp; Jessica's hand went to Paul's shoulder, tightened there. For a heartbeat,
		fear pulsed through her palm. Then she had herself under control. "Thus he has been taught, Your
		Reverence."
	</p>
	<p>&nbsp;&nbsp;&nbsp; <i>What does she fear?</i> Paul wondered.</p>
